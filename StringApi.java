package com.java.string;
public class StringApi {
	public static void main(String args[])
	{
		int i=10;
		int k=10;
		double j=12.2;
		double value = 45.0;
		System.out.println("absolute value of j is:"+Math.min(i,j));
		System.out.println("cos(" +value+ ") is:"+Math.cos(value));
		System.out.println("power value is:"+Math.pow(i,k));
		for(int s=0;s<6;s++)
		{
			System.out.println("square root values are:"+Math.sqrt(s));
		}
	}
}
